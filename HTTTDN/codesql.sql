-- 1.1. CUSTOMER TABLE
CREATE TABLE Customer (
    CustomerID INT PRIMARY KEY,
    CustomerName VARCHAR(150) NOT NULL,
    Email VARCHAR(150),
    Phone VARCHAR(20),
    CustomerType VARCHAR(50),
    Country VARCHAR(100),
    NumberOfPeople INT,
    Notes TEXT
);

-- 1.2. TOUR TABLE
CREATE TABLE Tour (
    TourID VARCHAR(10); PRIMARY KEY,
    TourCode VARCHAR(50) UNIQUE NOT NULL,
    TourName VARCHAR(200) NOT NULL,
    Destination VARCHAR(150),
    Duration INT,
    Description TEXT,
    BasePrice DECIMAL(12,2)
);

-- 1.3. BOOKING TABLE
CREATE TABLE Booking (
    BookingID INT PRIMARY KEY,
    BookingDate DATE NOT NULL,
    BookingStatus VARCHAR(50) NOT NULL,
    TravelDate DATE NOT NULL,
    NumberOfPeople INT NOT NULL,
    TotalAmount DECIMAL(12,2) NOT NULL,
    PaymentStatus VARCHAR(50) NOT NULL,
    CustomerID INT NOT NULL,
    TourID INT NOT NULL,

    CONSTRAINT fk_booking_customer
        FOREIGN KEY (CustomerID) REFERENCES Customer(CustomerID)
        ON DELETE CASCADE ON UPDATE CASCADE,

    CONSTRAINT fk_booking_tour
        FOREIGN KEY (TourID) REFERENCES Tour(TourID)
        ON DELETE CASCADE ON UPDATE CASCADE
);

-- 1.4. ACTIVITY TABLE
CREATE TABLE Activity (
    ActivityID INT PRIMARY KEY AUTO_INCREMENT,
    ActivityType VARCHAR(100) NOT NULL,
    ActivityDate DATETIME NOT NULL,
    Description TEXT,
    Rating INT,
    CustomerID INT NOT NULL,
    RelatedBookingID INT,

    CONSTRAINT fk_activity_customer
        FOREIGN KEY (CustomerID) REFERENCES Customer(CustomerID)
        ON DELETE CASCADE ON UPDATE CASCADE,

    CONSTRAINT fk_activity_booking
        FOREIGN KEY (RelatedBookingID) REFERENCES Booking(BookingID)
        ON DELETE SET NULL ON UPDATE CASCADE
);

-- 1.5 FEEDBACK TABLE
CREATE TABLE Feedback (
    FeedbackID INT PRIMARY KEY AUTO_INCREMENT,
    Rating INT NOT NULL,
    Comment TEXT,
    FeedbackDate DATE NOT NULL,
    Category VARCHAR(100),
    CustomerID INT NOT NULL,
    BookingID INT NOT NULL,

    CONSTRAINT fk_feedback_customer
        FOREIGN KEY (CustomerID) REFERENCES Customer(CustomerID)
        ON DELETE CASCADE ON UPDATE CASCADE,

    CONSTRAINT fk_feedback_booking
        FOREIGN KEY (BookingID) REFERENCES Booking(BookingID)
        ON DELETE CASCADE ON UPDATE CASCADE
);
